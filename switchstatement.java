
public class switchstatement {

	public static void main(String[] args) {

		int age = 4;
		switch (age) {

		case 1:
			System.out.println("Hello one");
			break;

		case 2:
			System.out.println("Hello two");
			break;

		case 3:
			System.out.println("Hello 3");
			break;

		default:
			System.out.println(" Hello Hello");
			break;
		}
	}

}
